from langchain_core.prompts import ChatPromptTemplate

# 图像识别提示词模板
IMAGE_RECOGNITION_TEMPLATE = """
请仔细观察并描述图片内容。要求如下：

1. 单据信息识别：
   - 单据标题
   - 单据日期
   - 单据编号
   - 单据类型
   - 单据备注
   - 货主信息
   - 仓库信息

2. 表格识别要求：
   - 首先识别图片中的表格结构，包括表头、行数、列数
   - 详细解释表格中所有数据所表达的含义，确保内容准确、逻辑清晰
   - 以分层结构输出表格内容，每一层代表一个单元格合并区域或单元项
   - 采用缩进或分组表示层级关系
   - 附加必要的行合并/列合并说明，但保持语义连贯
   - 禁止碎片化输出，不可遗漏任何单元格信息

3. 其他要求：
   - 如果图片中包含多个表格，请分别说明每个表格的内容
   - 如果表格跨页，请说明表格的连续性
   - 如果数据有单位，请一并说明
   - 如果数据有特殊格式（如日期格式），请保持原格式
   - 对于表格中的每一行数据，请完整提取所有字段信息
"""

image_recognition_prompt = ChatPromptTemplate.from_template(IMAGE_RECOGNITION_TEMPLATE) 